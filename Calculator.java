class Calculator{
	public static int add(int num1, int num2, int num3, int num4){
		int sum= num1+num2+num3+num4;
	return sum;	
	}
	public static float add(float number1, float number2, float number3, float number4){
	float sub= number1-number2-number3-number4;
	return sub;	
	}
	public static double add(double number1, double number2, double number3, double number4){
	double mult= number1*number2*number3*number4;
	return mult;	
	}
	public static long add(long number1, long number2, long number3, long number4){
	long div= number1/number2/number3/number4;
	return div;	
	}
}